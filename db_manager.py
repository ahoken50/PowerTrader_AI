import sqlite3
import os
import json
import time
from typing import Dict, Any, Optional

DATABASE_NAME = "bot_database.db"

def get_connection(db_path: str = DATABASE_NAME) -> sqlite3.Connection:
    """Returns a connection to the SQLite database with dict row factory."""
    conn = sqlite3.connect(db_path)
    conn.row_factory = sqlite3.Row
    return conn

def init_db(db_path: str = DATABASE_NAME) -> None:
    """Creates the necessary tables if they don't exist."""
    conn = get_connection(db_path)
    cursor = conn.cursor()
    
    # Settings table: Key-Value store for global bot settings
    cursor.execute('''
        CREATE TABLE IF NOT EXISTS settings (
            key TEXT PRIMARY KEY,
            value TEXT NOT NULL,
            updated_at REAL NOT NULL
        )
    ''')
    
    # Signals table: Stores LONG/SHORT DCA signals generated by pt_thinker
    cursor.execute('''
        CREATE TABLE IF NOT EXISTS signals (
            symbol TEXT PRIMARY KEY,
            long_level INTEGER NOT NULL DEFAULT 0,
            short_level INTEGER NOT NULL DEFAULT 0,
            updated_at REAL NOT NULL
        )
    ''')
    
    # Price Levels table: Stores the calculated bounding lines (N1-N7) from pt_thinker
    cursor.execute('''
        CREATE TABLE IF NOT EXISTS price_levels (
            symbol TEXT PRIMARY KEY,
            long_levels_json TEXT NOT NULL,
            short_levels_json TEXT NOT NULL,
            updated_at REAL NOT NULL
        )
    ''')
    
    # Process Status: Replaces runner_ready.json, used for IPC readiness
    cursor.execute('''
        CREATE TABLE IF NOT EXISTS process_status (
            process_name TEXT PRIMARY KEY,
            is_ready INTEGER NOT NULL DEFAULT 0,
            last_heartbeat REAL NOT NULL
        )
    ''')

    conn.commit()
    conn.close()

# --- SETTINGS CRUD ---
def set_setting(key: str, value: Any, db_path: str = DATABASE_NAME) -> None:
    conn = get_connection(db_path)
    cursor = conn.cursor()
    cursor.execute('''
        INSERT INTO settings (key, value, updated_at) 
        VALUES (?, ?, ?)
        ON CONFLICT(key) DO UPDATE SET 
            value=excluded.value, 
            updated_at=excluded.updated_at
    ''', (key, json.dumps(value), time.time()))
    conn.commit()
    conn.close()

def get_setting(key: str, default: Any = None, db_path: str = DATABASE_NAME) -> Any:
    conn = get_connection(db_path)
    cursor = conn.cursor()
    cursor.execute('SELECT value FROM settings WHERE key = ?', (key,))
    row = cursor.fetchone()
    conn.close()
    if row:
        try:
            return json.loads(row['value'])
        except Exception:
            return default
    return default

