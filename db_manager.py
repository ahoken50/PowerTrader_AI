# --- PROCESS STATUS CRUD ---
def set_process_ready(process_name: str, is_ready: bool, db_path: str = DATABASE_NAME) -> None:
    conn = get_connection(db_path)
    cursor = conn.cursor()
    cursor.execute('''
        INSERT INTO process_status (process_name, is_ready, last_heartbeat) 
        VALUES (?, ?, ?)
        ON CONFLICT(process_name) DO UPDATE SET 
            is_ready=excluded.is_ready,
            last_heartbeat=excluded.last_heartbeat
    ''', (process_name, 1 if is_ready else 0, time.time()))
    conn.commit()
    conn.close()

def is_process_ready(process_name: str, stale_seconds: int = 60, db_path: str = DATABASE_NAME) -> bool:
    """Returns True if process marked ready AND heartbeat has been updated recently."""
    conn = get_connection(db_path)
    cursor = conn.cursor()
    cursor.execute('SELECT is_ready, last_heartbeat FROM process_status WHERE process_name = ?', (process_name,))
    row = cursor.fetchone()
    conn.close()
    
    if row:
        ready = bool(row['is_ready'])
        last_beat = float(row['last_heartbeat'])
        if ready and (time.time() - last_beat) <= stale_seconds:
            return True
    return False

