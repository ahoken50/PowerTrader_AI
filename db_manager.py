def update_signals(symbol: str, long_level: int, short_level: int, db_path: str = DATABASE_NAME) -> None:
    conn = get_connection(db_path)
    cursor = conn.cursor()
    cursor.execute('''
        INSERT INTO signals (symbol, long_level, short_level, updated_at) 
        VALUES (?, ?, ?, ?)
        ON CONFLICT(symbol) DO UPDATE SET 
            long_level=excluded.long_level, 
            short_level=excluded.short_level,
            updated_at=excluded.updated_at
    ''', (symbol, long_level, short_level, time.time()))
    conn.commit()
    conn.close()

def get_signal(symbol: str, db_path: str = DATABASE_NAME) -> Dict[str, int]:
    conn = get_connection(db_path)
    cursor = conn.cursor()
    cursor.execute('SELECT long_level, short_level FROM signals WHERE symbol = ?', (symbol,))
    row = cursor.fetchone()
    conn.close()
    if row:
         return {"long": row['long_level'], "short": row['short_level']}
    return {"long": 0, "short": 0}

